package com.epam.tour.entity;

/**
 * The type Entity.
 */
public abstract class Entity {

    /**
     * The Id.
     */
    private int id;

    /**
     * Gets id.
     *
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * Sets id.
     *
     * @param id the id
     */
    public void setId(int id) {
        this.id = id;
    }

}
